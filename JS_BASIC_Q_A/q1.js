// Create a variable of type string and try to add a number to it.

// Creating a variable of type string (text data)
let a = "Millionaire";

// Creating a variable of type number
let b = 3;

// Add the string and number together

// In JS, when you use + with a string and a number,
// the number is converted to a string and both are joined (concatenated).
let sum = a + b;

// Print the result to the console
console.log(sum); //console.log() is a built-in function in JavaScript that is used to print output to the console.

// This will show: "Millionaire3"
