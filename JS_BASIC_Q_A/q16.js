// EXPLORE THE INCLUDES, STARTSWITH AND ENDSWITH FUNCTIONS OF A STRING

let millionaire = "Meet";

// includes() function checks if a string contains a certain substring
// Returns true if found, false if not
console.log(millionaire.includes("e")); // true → "Meet" has "e" in it
// console.log() is a built-in function in JavaScript that is used to print output to the console.

// startsWith() function checks if the string begins with a certain substring
console.log(millionaire.startsWith("M")); // true → "Meet" starts with "M"

// endsWith() function checks if the string ends with a certain substring
console.log(millionaire.endsWith("t")); // true → "Meet" ends with "t"
