// print "you can drive" and "you cannot drive" based on age being greater than 18 using ternary operator.

// Let's set an age to check
let age = 18; // Change this number to test different cases

// The ternary operator (? :) is a shorthand for if-else
      
// Syntax: condition ? doThisIfTrue : doThisIfFalse
age >= 18
      ? console.log("You can drive!!") // runs if age is 18 or more
      : console.log("You cannot drive!!"); // runs if age is less than 18
      //console.log() is a built-in function in JavaScript that is used to print output to the console.
